/** Заголовок в печати / предпросмотре (как в документе). */
export function smetaVariantPrintTitle(variant: SmetaVariantId): string {
  switch (variant) {
    case 'construction':
      return 'СМЕТА НА СТРОИТЕЛЬНО-РЕМОНТНЫЕ РАБОТЫ'
    case 'shower-enclosure':
      return 'СМЕТА НА УСТАНОВКУ ДУШЕВЫХ ОГРАЖДЕНИЙ'
    case 'cctv-basic':
      return 'СМЕТА НА ПОДБОР И МОНТАЖ ВИДЕОНАБЛЮДЕНИЯ (БАЗОВЫЙ УРОВЕНЬ, АРХИВ 14 СУТОК)'
    case 'cctv-optimal':
      return 'СМЕТА НА ПОДБОР И МОНТАЖ ВИДЕОНАБЛЮДЕНИЯ (ОПТИМАЛЬНЫЙ УРОВЕНЬ, АРХИВ 30 СУТОК)'
    default:
      return 'СМЕТА'
  }
}

export function normalizeSmetaVariant(v: unknown): SmetaVariantId {
  if (v === 'shower-enclosure') return 'shower-enclosure'
  if (v === 'cctv-optimal') return 'cctv-optimal'
  // «cctv» — старые сохранения до разделения на два уровня
  if (v === 'cctv-basic' || v === 'cctv') return 'cctv-basic'
  return 'construction'
}

